# 算术运算
a = 10
b = 3
print(a + b)   # 加法: 13
print(a - b)   # 减法: 7
print(a * b)   # 乘法: 30
print(a / b)   # 除法: 3.333...
print(a // b)  # 整除: 3
print(a % b)   # 取余: 1
print(a ** b)  # 幂运算: 1000

# 比较运算
print(a > b)   # True
print(a <= b)  # False

# 逻辑运算
x = True
y = False
print(x and y) # False
print(x or y)  # True
print(not x)   # False
